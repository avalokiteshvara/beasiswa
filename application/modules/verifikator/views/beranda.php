    Kegiatan bantuan beasiswa prestasi merupakan hal mendasar yang harus terlaksana dengan baik dan lancar. Untuk mendukung kelancaran pelaksanaan kegiatan bantuan beasiswa prestasi    diperlukan pendanaan yang memadai sesuai kebutuhan mahasiswa dan kemampuan daerah. Pendanaan untuk kegiatan bantuan beasiswa prestasi berasal dari Anggaran Pendapatan Belajaran Daerah (APBD) Provinsi Jambi.
